// Convert 24-hour time format to 12-hour time format with AM/PM
const convertTime = (time) => {
  // Splitting the input time string into hours and minutes
  const timeParts = time.split(":");
  let hours = parseInt(timeParts[0]);
  const minutes = parseInt(timeParts[1]);

  // ---------------------------------------------------------------------------------------------------------------------
  let meridiem = "am";
  
  // Checking if hours are greater than or equal to 12 to set meridiem to "pm"
  if (hours >= 12) {
    meridiem = "pm";
    if (hours > 12) {
      hours -= 12; // Converting hours to 12-hour format
    }
  }

  // ---------------------------------------------------------------------------------------------------------------------
  return (
    hours.toString().padStart(2) +
    ":" +
    minutes.toString().padStart(2, "0") +
    " " +
    meridiem
  );
};
// ---------------------------------------------------------------------------------------------------------------------

//------------------------------------------------------------------------------------------------------------------------
// Exporting convertTime // ---------------------------------------------------------------------------------------------------------------------
export default convertTime; // ---------------------------------------------------------------------------------------------------------------------
